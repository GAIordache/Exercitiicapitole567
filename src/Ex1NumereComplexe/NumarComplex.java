package Ex1NumereComplexe;

public class NumarComplex {
	private float mReal;
	private float mImaginar;
	
	public void setReal(float pReal) {
		this.mReal = pReal;
	}
	
	public float getReal() {
		return mReal;
	}

	public float getImaginar() {
		return mImaginar;
	}

	public void setImaginar(float pImaginar) {
		mImaginar = pImaginar;
	}
	
	
}
